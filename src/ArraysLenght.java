public class ArraysLenght {
    public static void main(String [] args){


        int test[] = new int[10];

        for (int k=0; k<test.length; k++){


            System.out.println(test[k]);
            for (int l=0; l<10; l++) {
                System.out.println(test[l]);
            }
        }


    }
}
